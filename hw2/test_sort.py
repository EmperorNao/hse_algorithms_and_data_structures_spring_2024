from sorter import radix_sort


def test_empty_sort():
    seq = []
    ret = radix_sort(seq)
    assert len(ret) == 0


def test_lexicographic_sort():
    seq = [
        "+7-495-1123213",
        "+375-123-1234568",
        "+7-495-1123212",
        "+375-123-2234567",
    ]
    gt_ret = [
        "+7-495-1123212",
        "+7-495-1123213",
        "+375-123-1234568",
        "+375-123-2234567",
    ]
    ret = radix_sort(seq)
    assert ret == gt_ret


def test_hard_sort():
    seq = [
        "+375-123-1234567",
        "+376-123-1234567",
        "+7-123-4567899",
        "+7-123-4567890",
        "+7-124-4567890",
        "+7-024-4567890",
        "+7-234-4567890",
        "+7-000-0000000",
        "+7-010-0000000",
        "+8-918-5197432",
        "+7-918-5197432",
        "+7-917-5197432",
    ]
    gt_ret = [
        "+7-000-0000000",
        "+7-010-0000000",
        "+7-024-4567890",
        "+7-123-4567890",
        "+7-123-4567899",
        "+7-124-4567890",
        "+7-234-4567890",
        "+7-917-5197432",
        "+7-918-5197432",
        "+8-918-5197432",
        "+375-123-1234567",
        "+376-123-1234567",
    ]
    ret = radix_sort(seq)
    assert ret == gt_ret
